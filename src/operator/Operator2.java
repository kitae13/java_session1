package operator;

public class Operator2 {
    public static void main(String[] args) {
        //문자열끼리 덧셈
        String str1 = "Hello";
        String str2 = "World";
        String result1 = str1 + str2;
        System.out.println(result1);

        //문자열과 정수형 덧셈
        String str3 = "2 x 3 = ";
        int a = 6;
        String result2 = str3 + a;
        System.out.println(result2);
    }
}